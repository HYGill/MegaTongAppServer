package model;
import java.util.ArrayList;

public class PhotoContent {
	int count;
	ArrayList<PhotoInfo> photos;

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public ArrayList<PhotoInfo> getPhotos() {
		return photos;
	}
	public void setPhotos(ArrayList<PhotoInfo> photos) {
		this.photos = photos;
	}
	
	
}
